import java.util.Scanner;

public class DistanceAndLine {

    // Method to find Euclidean distance
    public static double findDistance(
            double x1, double y1, double x2, double y2) {

        double distance = Math.sqrt(
                Math.pow(x2 - x1, 2) + Math.pow(y2 - y1, 2)
        );

        return distance;
    }

    // Method to find slope and y-intercept
    // return array: index 0 → slope, index 1 → y-intercept
    public static double[] findLineEquation(
            double x1, double y1, double x2, double y2) {

        double m = (y2 - y1) / (x2 - x1);
        double b = y1 - (m * x1);

        return new double[]{m, b};
    }

    public static void main(String[] args) {

        Scanner sc = new Scanner(System.in);

        // Input points
        System.out.println("Enter x1 and y1:");
        double x1 = sc.nextDouble();
        double y1 = sc.nextDouble();

        System.out.println("Enter x2 and y2:");
        double x2 = sc.nextDouble();
        double y2 = sc.nextDouble();

        // Calculate distance
        double distance = findDistance(x1, y1, x2, y2);
        System.out.println("\nEuclidean Distance = " + distance);

        // Calculate line equation
        double[] line = findLineEquation(x1, y1, x2, y2);

        System.out.println("Slope (m) = " + line[0]);
        System.out.println("Y-intercept (b) = " + line[1]);
        System.out.println("Equation of line: y = "
                + line[0] + "x + " + line[1]);
    }
}
